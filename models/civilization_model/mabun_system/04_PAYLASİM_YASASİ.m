Fazlanın Dolaşıma Girmesi ve Dengenin Korunması
GİRİŞ: PAYLAŞIM NEDEN EKONOMİNİN MERKEZİNDEDİR?
Modern sistem paylaşımı çoğu zaman:
yardım
hayır
fedakârlık
olarak görür.
Rasterast yaklaşımında ise paylaşım:
➡️ ahlakî bir seçenek değil, ontolojik zorunluluktur.
Çünkü varlık:
birikir
fakat aynı zamanda dolaşır.
Dolaşmayan birikim:
dengeyi bozar.
I — DOĞADA PAYLAŞIM İLKESİ
Doğa hiçbir fazlalığı sonsuza kadar tutmaz:
ağaç meyvesini bırakır
bulut suyu bırakır
beden fazlayı atar
ekosistem fazlalığı dağıtır.
Bu nedenle:
Fazla = dolaşıma girmesi gereken enerji.
Toplum da aynı yasaya tabidir.
II — PAYLAŞIM SADAKA DEĞİLDİR
Mabûn’a göre paylaşım:
lütuf değildir
üstünlük göstergesi değildir
güç ilişkisi değildir.
Paylaşım:
➡️ sistemin sağlıklı çalışması için zorunlu akıştır.
Bir yerde aşırı birikim varsa:
başka bir yerde eksiklik oluşur.
Bu, ahlakî olduğu kadar ontolojik bir sorundur.
III — FAZLANIN TANIMI
Fazla nedir?
yaşamı sürdürmek için gerekli olandan fazlası.
Bu:
para olabilir
gıda olabilir
bilgi olabilir
güç olabilir.
Mabûn şunu söyler:
Fazla, sahibinin değildir; dolaşımın hakkıdır.
IV — PAYLAŞIMSIZ TOPLUMUN SONUÇLARI
Paylaşım durduğunda:
servet merkezileşir
güç yoğunlaşır
güven azalır.
Toplumda:
kıskançlık artar
sosyal gerilim yükselir
ahlak zayıflar.
Bu durum:
ekonomik krizden önce gelir.
V — PAYLAŞIM VE ADALET
Paylaşım eşitlik değildir.
Rasterast:
herkese aynı sonucu değil
dengeli dolaşımı amaçlar.
Adalet:
➡️ akışın doğru yönlendirilmesidir.
Bu nedenle:
emek korunur
ihtiyaç gözetilir
üretim teşvik edilir.
VI — PAYLAŞIMIN MEKANİKLERİ
Mabûn’da paylaşım:
1️⃣ doğal dolaşım (ticaret)
2️⃣ toplumsal dayanışma
3️⃣ zorunlu dengeleme mekanizmaları
üzerinden gerçekleşir.
Amaç:
zenginliği yok etmek değil,
durağanlaşmasını engellemektir.
VII — PAYLAŞIM VE ONUR
Gerçek paylaşım:
verenin üstünlüğü
alanın küçülmesi
anlamına gelmez.
Çünkü:
➡️ bugün veren yarın alan olabilir.
Bu nedenle Mabûn’da:
paylaşım gizlidir, gösteri değildir.
VIII — BİLGİ PAYLAŞIMI
Sadece servet değil, bilgi de paylaşılmalıdır.
Bilgi tek elde toplanırsa:
manipülasyon artar
toplum yönsüzleşir.
Zanabûn ile Mabûn burada birleşir:
Bilgi ve servet aynı yasa ile dolaşmalıdır.
IX — PAYLAŞIM VE NESİLLER
Paylaşım sadece yaşayanlar arasında değildir.
Gelecek nesiller:
dolaşım zincirinin parçasıdır.
Bu yüzden:
toprağı tüketmek
suyu kirletmek
kaynakları yok etmek
paylaşım ihlalidir.
X — PAYLAŞIM YASASININ ÖZÜ
Paylaşım:
sistemi yavaşlatmaz
aksine istikrar sağlar.
Denge:
birikimle değil,
➡️ akışla korunur.
SONUÇ
Mabûn’un paylaşım ilkesi şunu sabitler:
Fazla, dolaşım içindir.
Paylaşım yoksa:
güven yoktur
ekonomi yoktur
toplum uzun süre yaşayamaz.
