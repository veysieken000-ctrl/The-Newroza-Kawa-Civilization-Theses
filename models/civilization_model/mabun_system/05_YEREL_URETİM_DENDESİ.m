Coğrafya, Üretim ve Ekonomik Bağımsızlığın Ontolojik Temeli
GİRİŞ: ÜRETİM NEDEN YEREL BAŞLAMAK ZORUNDADIR?
Mabûn yaklaşımında ekonomi:
merkezî birikim değil
dağıtılmış denge düzenidir.
Bu nedenle üretim:
➡️ önce yaşanılan yerde anlam kazanır.
Çünkü varlık:
kendi çevresinde kök salar
yakın çevresiyle ilişki kurar
sonra genişler.
Yerel üretim:
ekonomik tercih değil,
ontolojik uyumdur.
I — COĞRAFYANIN SINIRI, ÜRETİMİN ÖLÇÜSÜ
Her coğrafya:
iklim
su
toprak
biyolojik çeşitlilik
bakımından farklıdır.
Bu farklılık:
➡️ üretimin sınırını belirler.
Mabûn’a göre hata şudur:
coğrafyanın kaldıramayacağı üretimi dayatmak.
Sonuç:
toprak tükenir
insan bağımlı hâle gelir
ekonomi kırılganlaşır.
II — YEREL ÜRETİMİN ONTOLOJİK GEREKÇESİ
Doğadaki sistemler:
enerjiyi uzaklardan taşımaz
yakın çevrede dengeler.
Hücre:
ihtiyacını kendi içinde üretir.
Toplum da benzer şekilde:
➡️ temel ihtiyaçlarını yerelde karşılamalıdır.
Bu:
güvenliktir
sürekliliktir
bağımsızlıktır.
III — MERKEZİLEŞMENİN RİSKİ
Üretim tek merkezde toplandığında:
çevre bağımlı olur
kırılganlık artar
krizler zincirleme yayılır.
Bir merkez durduğunda:
bütün sistem sarsılır.
Bu yüzden Mabûn:
tek merkezli ekonomik modeli riskli görür.
IV — YEREL ÜRETİM ≠ KAPALI EKONOMİ
Yerel üretim:
dış ticareti reddetmez
paylaşımı engellemez.
Ama şu ilkeyi koyar:
➡️ önce yaşamak, sonra büyümek.
Yani:
temel gıda
temel enerji
temel ihtiyaçlar
yerelden sağlanmalıdır.
V — TAŞIMA MALİYETİ VE AHLAK
Uzun mesafeli üretim:
enerji tüketir
doğayı zorlar
gizli maliyet üretir.
Mabûn bu nedenle:
uzak üretimin sürekli hâle gelmesini
ahlaki risk olarak görür.
Çünkü:
bir yerde ucuzluk,
başka bir yerde tahribat anlamına gelebilir.
VI — YEREL ÜRETİM VE TOPLUMSAL BAĞ
Üretim yerelde olduğunda:
insanlar emeği görür
üretici ile tüketici arasındaki mesafe azalır
güven artar.
Bu durum:
ekonomiyi ahlakla bağlar.
Görünmeyen üretim:
sorumluluğu zayıflatır.
VII — ÇEŞİTLİLİK VE DAYANIKLILIK
